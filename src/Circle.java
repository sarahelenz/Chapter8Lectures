public class Circle {
    public static int x = 0;
    public static int y = 0;
    public int radius;

    public void setRadius(int radius){
        this.radius = radius;
    }
    public int getRadius(){
        return this.radius;
    }

    public String toString(){
        return "(x - 0)^2 + (y - 0)^2 = 2^2";
    }
}
